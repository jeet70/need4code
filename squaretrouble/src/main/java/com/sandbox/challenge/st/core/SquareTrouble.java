    private  Set findAdjacent(int position)
	{
		Set decidingSet = new HashSet();
		Set adjacentSet = new HashSet();
		
		List lst1 = new ArrayList();
		lst1.add(1);
		lst1.add(2);
		lst1.add(3);
		if(lst1.contains(position))decidingSet.add(lst1);
		
		List lst2 = new ArrayList();
		lst2.add(4);
		lst2.add(5);
		lst2.add(6);
		if(lst2.contains(position))decidingSet.add(lst2);
		
		List lst3 = new ArrayList();
		lst3.add(7);
		lst3.add(8);
		lst3.add(9);
		if(lst3.contains(position))decidingSet.add(lst3);
		
		List lst4 = new ArrayList();
		lst4.add(1);
		lst4.add(4);
		lst4.add(7);
		if(lst4.contains(position))decidingSet.add(lst4);
		
		List lst5 = new ArrayList();
		lst5.add(2);
		lst5.add(5);
		lst5.add(8);
		if(lst5.contains(position))decidingSet.add(lst5);
		
		List lst6 = new ArrayList();
		lst6.add(3);
		lst6.add(6);
		lst6.add(9);
		if(lst6.contains(position))decidingSet.add(lst6);
		
		Iterator iterator = decidingSet.iterator();
		while(iterator.hasNext())
		{
			List lst = (ArrayList)iterator.next();
			int index= lst.indexOf(position);
			if(index==0)
			{
				adjacentSet.add(lst.get(1));
			}
			else if (index==2)
			{
				adjacentSet.add(lst.get(1));
			}
			else if (index==1)
			{
				adjacentSet.add(lst.get(0));
				adjacentSet.add(lst.get(2));
			}
			
		}
		return adjacentSet;
	}

    
