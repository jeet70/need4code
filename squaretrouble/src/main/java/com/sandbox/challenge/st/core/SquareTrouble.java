    private void provideSides(int cellNumber)
    {
        countMove++;
        if(player1)
        {
            startWith = 'O';
            player1 = false;
            lastMove1 = cellNumber;
        }
        else
        {
            startWith = 'X';
            player1 = true;
            lastMove2 = cellNumber;
        }
        if(countMove>1)
        {
            int lastMove =player1?lastMove1:lastMove2;
            changeAccording(false);
            Set adjacentSide = findAdjacent(lastMove);
            Iterator iterate = adjacentSide.iterator();
            while(iterate.hasNext())
            {
                enableFew((Integer) iterate.next(),true);
            }
            
        }
                declareTurn();
    }
    private void declareTurn()
    {
        if(jButton1.isEnabled()||jButton2.isEnabled()||jButton3.isEnabled()||jButton4.isEnabled()||
                jButton5.isEnabled()||jButton6.isEnabled()||jButton7.isEnabled()||jButton8.isEnabled()||
                jButton9.isEnabled())
        {
            if(player1)
            {
                jLabel1.setText("Player1's turn");
            }
            else
            {
                jLabel1.setText("Player2's turn");
            }
        }
        else
        {
           if(player1)
            {
                jLabel1.setText("Player2 wins");
            }
            else
            {
                jLabel1.setText("Player1 wins");
            } 
        }
    }
    
